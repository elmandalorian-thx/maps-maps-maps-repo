// Helper functions
export const getCountries = (): string[] => {
  return LOCATIONS.map((c) => c.name);
};

export const getProvinces = (countryName: string): Province[] => {
  const country = LOCATIONS.find((c) => c.name === countryName);
  return country?.provinces || [];
};

export const getCities = (countryName: string, provinceName: string): City[] => {
  const country = LOCATIONS.find((c) => c.name === countryName);
  const province = country?.provinces.find((p) => p.name === provinceName);
  return province?.cities || [];
};

export const getAllCitiesInCountry = (countryName: string): { province: string; city: string }[] => {
  const country = LOCATIONS.find((c) => c.name === countryName);
  if (!country) return [];

  const cities: { province: string; city: string }[] = [];
  country.provinces.forEach((province) => {
    province.cities.forEach((city) => {
      cities.push({ province: province.name, city: city.name });
    });
  });
  return cities;
};

export const getAllCitiesInProvince = (countryName: string, provinceName: string): string[] => {
  const cities = getCities(countryName, provinceName);
  return cities.map((c) => c.name);
};
